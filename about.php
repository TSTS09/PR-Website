                    <h2 class="section-title">The FNG Story</h2>
                    <p class="section-description">
                        Fashion Nexus Ghana (FNG) is a not-for-profit platform dedicated to scaling and developing Ghana's fashion industry.
                    </p>
                    
                    <p>
                        Born from over 14 years of hands-on work and advocacy, our journey began in 2012 when we led public relations for Ghana Fashion & Design Week in partnership with Vogue Italia - a milestone that placed Ghana on the global fashion map.
                    </p>
                    
                    <p>
                        Over the next decade, we trained designers, hosted knowledge exchanges with global brands, advised boards and universities, and facilitated collaborations that expanded visibility and built capacity across the sector.
                    </p>
                    
                    <p>
